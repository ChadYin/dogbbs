package com.db.model;

public class User {
	private int u_id;
	private String u_name;
	private String u_email;
	private String u_password;
	private int u_state;
	private int u_identity;
	private String u_realname;
	private String u_number;
	private String u_tips;

	public int getU_id() {
		return u_id;
	}

	public void setU_id(int u_id) {
		this.u_id = u_id;
	}

	public String getU_name() {
		return u_name;
	}

	public void setU_name(String u_name) {
		this.u_name = u_name;
	}

	public String getU_email() {
		return u_email;
	}

	public void setU_email(String u_email) {
		this.u_email = u_email;
	}

	public String getU_password() {
		return u_password;
	}

	public void setU_password(String u_password) {
		this.u_password = u_password;
	}

	public int getU_state() {
		return u_state;
	}

	public void setU_state(int u_state) {
		this.u_state = u_state;
	}

	public int getU_identity() {
		return u_identity;
	}

	public void setU_identity(int u_identity) {
		this.u_identity = u_identity;
	}

	public String getU_realname() {
		return u_realname;
	}

	public void setU_realname(String u_realname) {
		this.u_realname = u_realname;
	}

	public String getU_number() {
		return u_number;
	}

	public void setU_number(String u_number) {
		this.u_number = u_number;
	}

	public String getU_tips() {
		return u_tips;
	}

	public void setU_tips(String u_tips) {
		this.u_tips = u_tips;
	}

}
